    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        PeliculasDAO tipodao = new PeliculasDAO();
        Peliculas insertartipo = new Peliculas();
        insertartipo.setCodigo_Pelicula(txtCodigoP.getText());
        insertartipo.setNombre_Pelicula(txtNombreP.getText());
        insertartipo.setAutor_Pelicula(txtAutorP.getText());
        insertartipo.setGenero_Pelicula(txtGeneroP.getText());
        insertartipo.setClasificacion_Pelicula(txtClasificacionP.getText());
        insertartipo.setDuracion_Pelicula(txtDuracionP.getText());
        insertartipo.setPrecio_Pelicula(txtPrecioP.getText());
        insertartipo.setExistencias(txtExistenciaP.getText());
 
        insertartipo.setTienda(Cbox_CTienda.getSelectedItem().toString());
        tipodao.insert(insertartipo);
        try {
            llenadoDeTablas();
        } catch (SQLException ex) {
            Logger.getLogger(Mantenimiento_Peliculas.class.getName()).log(Level.SEVERE, null, ex);
        }
        limpiar();
    }//GEN-LAST:event_btnGuardarActionPerformed

